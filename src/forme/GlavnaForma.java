    private void ucitajKomboBoksKlijenta() {
        ArrayList<Klijent> listaKlijenta = Kontroler.getInstance().vratiKlijente();

        cmbKlijent.removeAllItems();
        for (Klijent klijent : listaKlijenta) {
            cmbKlijent.addItem(klijent);
        }
    }

    private void ucitajDrugiKombo() {
        ArrayList<VrstaPostupka> listaVP = Kontroler.getInstance().vratiListuPostupaka();

        cmbVrstaPostupka.removeAllItems();

        for (VrstaPostupka vrstaPostupka : listaVP) {
            cmbVrstaPostupka.addItem(vrstaPostupka);
        }
    }

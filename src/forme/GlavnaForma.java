    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        Klijent klijent = (Klijent) cmbKlijent.getSelectedItem();
        VrstaPostupka vp = (VrstaPostupka) cmbVrstaPostupka.getSelectedItem();
        String nazivP = txtNaziv.getText();
        String problem = txtProblem.getText();
        String dat = txtDatum.getText();
        if (dat != null && nazivP != null && problem != null) {
            if (advokat.getSpecijalnost().equals(vp.getNaziv())) {
                SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
                try {
                    Date datum = sdf.parse(dat);
                    Predmet predmet = new Predmet(-1, nazivP, problem, datum, advokat, klijent, vp);
                    ModelTabele mt = (ModelTabele) tabelaPredmeta.getModel();
                    mt.dodajPredmet(predmet);
                } catch (ParseException ex) {
                    Logger.getLogger(GlavnaForma.class.getName()).log(Level.SEVERE, null, ex);
                }

                
            } else {
                JOptionPane.showMessageDialog(this, "Advokat mora imati svoju specijalnost!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Morate uneti sva polja!");
        }

    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnIzbrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzbrisiActionPerformed
            int red = tabelaPredmeta.getSelectedRow();
            
            if(red!=-1) {
               ModelTabele mt = (ModelTabele) tabelaPredmeta.getModel();
               mt.obrisiPredmet(red);
            }
    }//GEN-LAST:event_btnIzbrisiActionPerformed

    private void btnSacuvajSveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajSveActionPerformed
        ModelTabele mt = (ModelTabele) tabelaPredmeta.getModel();
        int brojac = 0;
        ArrayList<Predmet> listaPredmeta = mt.dajPredmete();
        for (Predmet predmet : listaPredmeta) {
            for (Predmet predmet1 : listaPredmeta) {
                if(predmet!=predmet1 && predmet.getDatum()==predmet1.getDatum() && predmet.getAdvokat()==predmet1.getAdvokat()){
                    brojac++;
                }
            }
            if(brojac>3) {
                JOptionPane.showMessageDialog(this, "Mozete biti angazovani najvise za 3 predmeta! ");
                return;
            }
        }
        boolean sacuvano =Kontroler.getInstance().sacuvajPredmete(listaPredmeta);
        
        if(sacuvano){
         JOptionPane.showMessageDialog(this, "Uspesno sacuvano! ");
        } else {
             JOptionPane.showMessageDialog(this, "Doslo je do greske!");
        }
                
    }//GEN-LAST:event_btnSacuvajSveActionPerformed

